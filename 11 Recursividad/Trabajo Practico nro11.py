def potencia(base, exponente):
    if exponente == 0:
        return 1;
    else:
        return base * potencia(base, exponente - 1);

Base = int(input("Ingresa la base: "));
Exponente = int(input("Introduce el exponente: "));

resultado = potencia(Base, Exponente);
print(f"{Base} elevado a la {Exponente} es: {resultado}"); 

#4) Crear una funci√≥n recursiva en Python que reciba un n√∫mero entero positivo en base 
# decimal y devuelva su representaci√≥n en binario como una cadena de texto. 
def decimal(x):
    if x == 0:
        return "0"
    elif x == 1:
        return "1"
    else:
        return decimal(x // 2) + str(x % 2)

usuario = int(input(f"Ingrese un valor: ")); 

if usuario < 0:
    print("El n√∫mero debe ser positivo.")
else:
    binario = decimal(usuario)
    print(f"La representaci√≥n binaria de {usuario} es: {binario}") 


#5) Implement√° una funci√≥n recursiva llamada es_palindromo(palabra) que reciba una 
# cadena de texto sin espacios ni tildes, y devuelva True si es un pal√≠ndromo o False si no lo es. 
     #Requisitos: 
     # La soluci√≥n debe ser recursiva. 
     # No se debe usar [::-1] ni la funci√≥n reversed().

def es_palindromo(palabra):
    if len(palabra) <= 1:
        return True  
    if palabra[0] != palabra[-1]:
        return False 
    
    return es_palindromo(palabra[1:-1])

palabra = input("Introduce una palabra (sin espacios ni tildes): ").lower() ## para comparar que sean iguales 
if es_palindromo(palabra):
    print(f'"{palabra}" es un palindromo.')
else:
    print(f'"{palabra}" no es un palindromo.')


