#Escrib√≠ una funci√≥n recursiva contar_bloques(n) que reciba el n√∫mero de bloques en el 
# nivel m√°s bajo y devuelva el total de bloques que necesita para construir toda la pir√°mide. 
def contar_bloques(n):
    if n == 1:          
        return 1
    else:
        return n + contar_bloques(n - 1)

# Ejemplos de prueba
print(contar_bloques(1))  
print(contar_bloques(2))  
print(contar_bloques(4))  

##8) Escrib√≠ una funci√≥n recursiva llamada contar_digito(numero, digito) que reciba un 
# n√∫mero entero positivo (numero) y un d√≠gito (entre 0 y 9), y devuelva cu√°ntas veces aparece ese d√≠gito dentro del n√∫mero. 
      #Ejemplos: 
      # contar_digito(12233421, 2)   ‚Üí 3   
      # contar_digito(5555, 5)       ‚Üí 4   
      # contar_digito(123456, 7)     ‚Üí 0  

def contar_digito(numero, digito):
    if numero == 0:
        return 0
    else:
        ultimo = numero % 10
        return (1 if ultimo == digito else 0) + contar_digito(numero // 10, digito)

# Ejemplos de prueba
print(f"el numero 2 aparece {contar_digito(12233421, 2)}veces")  
print(contar_digito(5555, 5))      
print(contar_digito(123456, 7))    

