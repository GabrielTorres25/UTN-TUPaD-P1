#1) Crea una funci√≥n recursiva que calcule el factorial de un n√∫mero. Luego, utiliza esa 
#funci√≥n para calcular y mostrar en pantalla el factorial de todos los n√∫meros enteros 
#entre 1 y el n√∫mero que indique el usuario

def fact(n):
    if n == 0 or n == 1:
        return 1;
    else: 
        return n * fact(n - 1);
         
num = int(input("Ingrese un numero mayor que 1: ")); 

if num < 1:
    print("Porfavor ingrese un numero mayor que uno: "); 
else: 
    print(f"factoriales del 1 al {num}: ");
    for i in range(1, num + 1):
        print(f"{i} = {fact(i)}");

#2) Crea una funci√≥n recursiva que calcule el valor de la serie de Fibonacci en la posici√≥n 
# indicada. Posteriormente, muestra la serie completa hasta la posici√≥n que el usuario especifique. 

def fibonacci(n):
    if n == 0:
        return 0;
    elif n == 1:
        return 1;
    else:
        return fibonacci(n-1) + fibonacci(n-2);
    
posicion = int(input("Introduce una posicion de la serie de Fibonacci: ")); 
print("Serie de Fibonacci hasta la posicion", posicion, ":");
for i in range(posicion + 1):
    print(fibonacci(i));

