     # suma_digitos(9)      ‚Üí 9 
     # suma_digitos(305)    ‚Üí 8   (3 + 0 + 5) 

def suma_digitos(n): 
    if n < 10:
        return n 
    else:
        return (n % 10) + suma_digitos(n // 10)

